export function getLocalizedScam(scamId: string, languageCode: string, defaultScam: any): LocalizedScam {
  const langData = localizedData[languageCode];
  if (!langData) return defaultScam;
  
  const scamOverride = langData[scamId];
  if (!scamOverride) return defaultScam;

  return {
    ...defaultScam,
    ...scamOverride,
    redFlags: scamOverride.redFlags || defaultScam.redFlags
  };
}

export function getLocalizedScenarios(scenarios: any[], languageCode: string): any[] {
  return scenarios.map(s => getLocalizedScam(s.id, languageCode, s));
}
